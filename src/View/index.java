    public void verificarObjetos()
    {
        if (cm == null)
        {
            cm = new CadastrarMusica();
        }  
        
        if (ca == null)
        {
            ca = new CadastrarArtista();
        }  
                
        if (la == null)
        {
            la = new ListaAlbum();
        }  
        
        if (lpl == null)
        {
            lpl = new ListaPlaylist();
            Playlist pl = new Playlist();
            pl.setNome("Todas as Música");
            lpl.adicionarPlaylist(pl);
        }  
    }
        
