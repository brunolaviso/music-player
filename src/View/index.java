    private void btnRemoverMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnRemoverMousePressed
        // TODO add your handling code here:
        
        if (tabela.getSelectedRow() != -1)
        {
            int resposta = JOptionPane.showConfirmDialog(null, "Tem certeza que deseja excluir a música "+ cm.imprimirTitulo(tabela.getSelectedRow()).toString() + "?", "Confirmar exclusão", JOptionPane.YES_NO_OPTION);
            
            if (resposta == 0)
            {
                cm.removeMusica(tabela.getSelectedRow());
                //atualizar();
            }
        }
    }//GEN-LAST:event_btnRemoverMousePressed

    private void btnListarMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnListarMousePressed
        // TODO add your handling code here:
        
        if (listPlaylistis.getSelectedIndex() != -1)
        {
            if (listPlaylistis.getSelectedIndex()== 0)
            {
                tabela.setModel(cm);
            }
            else
            {
                tabela.setModel(lpl.exibicao(listPlaylistis.getSelectedIndex()));
            }
        }
        
    }//GEN-LAST:event_btnListarMousePressed

