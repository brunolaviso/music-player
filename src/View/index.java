    public void reproduzir(int i)
    {
        if (caminho.equals(tabela.getValueAt(i,2).toString()))
        {
            JOptionPane.showMessageDialog(null, "A Musica já está sendo Reproduzida");
        }
        else
        {
            caminho = tabela.getValueAt(i,2).toString();
            uriString = new File(caminho).toURI().toString();
            pick = new Media(uriString); 
            player = new MediaPlayer(pick);    
           
        }
    }
    
