    private void btnProcurarMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnProcurarMousePressed
        // TODO add your handling code here:
        
        JFileChooser fileChooser = new JFileChooser();
        fileChooser.setDialogTitle("Buscar Música");
        fileChooser.setFileSelectionMode(JFileChooser.FILES_ONLY);
        
        FileNameExtensionFilter ff = new FileNameExtensionFilter("Musica", "mp3", "wav");
        
        fileChooser.setFileFilter(ff);
        int retorno = fileChooser.showOpenDialog(this);
        
        if (retorno == JFileChooser.APPROVE_OPTION)
        {
            File file = fileChooser.getSelectedFile();
            txtCaminho.setText(file.getPath());
        }
    }//GEN-LAST:event_btnProcurarMousePressed

