    private void btnConcluirMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnConcluirMousePressed
        // TODO add your handling code here:
        
        if ((txtTitulo.getText().equals("")) || (txtCaminho.getText().equals("")))
        {
            JOptionPane.showMessageDialog(null, "Todos os Campos devem ser Preenchidos");
        }
        else
        {
            String titulo = txtTitulo.getText();
            String artista = cbArtista.getSelectedItem().toString();
            String caminho = txtCaminho.getText();
            cm.cadastrarMusicaOnlyTitulo(titulo, artista, caminho);

            JOptionPane.showMessageDialog(null, "Música cadastrada com sucesso!");

            formi.setVisible(true);
            //i.setLocationRelativeTo(null);
            formi.recebeObjetoMusica(cm);
            formi.recebeObjetoArtista(ca);
            this.dispose();
        }
        
    }//GEN-LAST:event_btnConcluirMousePressed

